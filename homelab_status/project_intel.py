# ── Agent detection regexes ───────────────────────────────────────────────────

# Co-authored-by patterns with model extraction
_CLAUDE_CO_AUTHOR_RE = re.compile(
    r'co-authored-by:\s+claude\s*(?P<model>[^\s<]*[^\s<,])?[^<]*<[^>]*anthropic\.com',
    re.IGNORECASE,
)
# Model name patterns embedded in Co-authored-by lines
_MODEL_NAME_RE = re.compile(
    r'(claude\s+(?:opus|sonnet|haiku)\s+[\d.]+|claude\s+(?:opus|sonnet|haiku))',
    re.IGNORECASE,
)
# Footer generated by Claude Code
_CLAUDE_CODE_FOOTER_RE = re.compile(
    r'(?:generated\s+with\s+\[?claude\s+code\]?|🤖\s+generated\s+with\s+\[?claude\s+code\]?)',
    re.IGNORECASE,
)
# Generic noreply@anthropic.com marker
_ANTHROPIC_EMAIL_RE = re.compile(r'noreply@anthropic\.com', re.IGNORECASE)

# Cursor markers
_CURSOR_RE = re.compile(
    r'(?:co-authored-by:\s+cursor|\.cursor/rules|cursor\s+tracking)',
    re.IGNORECASE,
)
# CodeRabbit markers
_CODERABBIT_RE = re.compile(
    r'(?:co-authored-by:\s+coderabbit|coderabbitai\[bot\])',
    re.IGNORECASE,
)
# GitHub Copilot
_COPILOT_RE = re.compile(r'co-authored-by:\s+github-copilot', re.IGNORECASE)
# Aider
_AIDER_RE = re.compile(r'co-authored-by:\s+aider', re.IGNORECASE)


def detect_agents(message: str) -> AgentAttribution:
    """
    Parse a commit message and return which AI tool(s) authored it.

    Detection precedence:
      1. Co-authored-by: Claude <model> <noreply@anthropic.com>  → claude-code
      2. 'Generated with Claude Code' footer                     → claude-code
      3. noreply@anthropic.com anywhere                          → claude-code
      4. Cursor markers                                          → cursor_used=True
      5. CodeRabbit markers                                      → coderabbit_reviewed=True
      6. Copilot / Aider                                         → other_agents
      7. Anything else                                           → human

    If both cursor and claude are detected → agent_tool='mixed'.
    """
    lower = message.lower()
    other_agents: list[str] = []
    cursor_used = False
    coderabbit_reviewed = False
    claude_detected = False
    claude_model = ""

    # ── Claude detection ────────────────────────────────────────────────────
    # Try the full Co-authored-by line first — it may carry the model name
    co_match = _CLAUDE_CO_AUTHOR_RE.search(message)
    if co_match or _ANTHROPIC_EMAIL_RE.search(message) or _CLAUDE_CODE_FOOTER_RE.search(message):
        claude_detected = True
        # Extract model name from the full message
        model_match = _MODEL_NAME_RE.search(message)
        if model_match:
            # Normalise capitalisation: "claude sonnet 4.6" → "Claude Sonnet 4.6"
            raw = model_match.group(0).strip()
            parts = raw.split()
            claude_model = " ".join(p.capitalize() if i < 2 else p for i, p in enumerate(parts))
        else:
            # Try to pull from the co-author token
            if co_match:
                token = (co_match.group("model") or "").strip()
                if token:
                    claude_model = token.replace("-", " ").title()

    # ── Cursor detection ─────────────────────────────────────────────────────
    if _CURSOR_RE.search(message):
        cursor_used = True

    # ── CodeRabbit detection ─────────────────────────────────────────────────
    if _CODERABBIT_RE.search(message):
        coderabbit_reviewed = True

    # ── Other tools ───────────────────────────────────────────────────────────
    if _COPILOT_RE.search(message):
        other_agents.append("copilot")
    if _AIDER_RE.search(message):
        other_agents.append("aider")

    # ── Resolve primary agent ─────────────────────────────────────────────────
    if claude_detected and cursor_used:
        agent_tool = "mixed"
    elif claude_detected:
        agent_tool = "claude-code"
    elif cursor_used:
        agent_tool = "cursor"
    elif "copilot" in other_agents:
        agent_tool = "copilot"
    elif "aider" in other_agents:
        agent_tool = "aider"
    else:
        agent_tool = "human"

    return AgentAttribution(
        agent_tool=agent_tool,
        claude_model=claude_model,
        cursor_used=cursor_used,
        coderabbit_reviewed=coderabbit_reviewed,
        other_agents=other_agents,
    )


