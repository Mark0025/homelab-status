# ── Bulk refresh ──────────────────────────────────────────────────────────────

_profile_lock = asyncio.Lock()
_profile_running = False


async def refresh_all_profiles(force: bool = False) -> dict:
    """
    Profile every repo in gh_repos and upsert into project_profiles.

    Uses semaphore(5) so we don't hammer the GitHub API.
    Safe to call from background tasks — acquires a module-level lock.

    Returns:
        {
            "status": "ok",
            "repos_scanned": N,
            "profiles_saved": N,
            "errors": N,
        }
    """
    global _profile_running
    if _profile_running and not force:
        return {"status": "already_running"}

    async with _profile_lock:
        _profile_running = True
        try:
            return await _do_refresh_profiles(force)
        finally:
            _profile_running = False


async def _do_refresh_profiles(force: bool = False) -> dict:
    _init_intel_tables()

    with _conn() as conn:
        repo_rows = conn.execute(
            "SELECT full_name, owner, name FROM gh_repos ORDER BY pushed_at DESC"
        ).fetchall()

    if not repo_rows:
        logger.warning("No repos in gh_repos — run git_history.refresh_all() first")
        return {"status": "ok", "repos_scanned": 0, "profiles_saved": 0, "errors": 0}

    sem = asyncio.Semaphore(5)
    errors = 0
    saved = 0

    async def _do_one(client: httpx.AsyncClient, owner: str, repo: str) -> bool:
        nonlocal errors, saved
        async with sem:
            try:
                profile = await profile_repo(client, owner, repo)
                _upsert_profile(profile)
                saved += 1
                logger.debug(f"  Profiled {owner}/{repo} — agent={profile['primary_agent']}")
                return True
            except Exception as exc:
                logger.warning(f"Profile failed for {owner}/{repo}: {exc}")
                errors += 1
                return False

    async with httpx.AsyncClient() as client:
        tasks = [
            _do_one(client, r["owner"], r["name"])
            for r in repo_rows
        ]
        await asyncio.gather(*tasks)

    logger.info(f"Profile refresh done: {saved} saved, {errors} errors, {len(repo_rows)} total")
    return {
        "status": "ok",
        "repos_scanned": len(repo_rows),
        "profiles_saved": saved,
        "errors": errors,
    }


# ── Query functions (synchronous, read-only) ──────────────────────────────────

def _deserialise_profile(row: sqlite3.Row | dict) -> dict:
    """Convert a raw DB row into a clean dict with JSON fields expanded."""
    d = dict(row)
    for field in ("tech_stack", "docker_networks", "connects_to", "other_agents"):
        try:
            d[field] = json.loads(d[field]) if d.get(field) else []
        except Exception:
            d[field] = []
    for field in ("cursor_used", "coderabbit_used", "claude_md_exists",
                  "readme_exists", "is_active", "is_fork",
                  "has_docker", "has_tests", "has_ci",
                  "has_pricing_ui", "has_payment_webhook"):
        if field in d:
            d[field] = bool(d[field])
    return d


def get_project_profile(repo: str, owner: str | None = None) -> dict | None:
    """
    Return the cached profile for a single repo, or None if not yet profiled.

    Args:
        repo:  Repository name (e.g. 'wes') OR full name (e.g. 'Mark0025/wes').
        owner: GitHub owner/org — inferred from `repo` if it contains '/'.
    """
    _init_intel_tables()

    # Allow 'owner/repo' as a single arg
    if "/" in repo and owner is None:
        owner, repo = repo.split("/", 1)

    with _conn() as conn:
        if owner:
            row = conn.execute(
                "SELECT * FROM project_profiles WHERE repo=? AND owner=?",
                (repo, owner),
            ).fetchone()
        else:
            row = conn.execute(
                "SELECT * FROM project_profiles WHERE repo=? ORDER BY profiled_at DESC LIMIT 1",
                (repo,),
            ).fetchone()

    return _deserialise_profile(row) if row else None


def get_all_profiles(active_only: bool = True) -> list[dict]:
    """
    Return all cached profiles.

    Args:
        active_only: When True (default) skip repos with no commits in 90 days.
    """
    _init_intel_tables()
    query = "SELECT * FROM project_profiles"
    if active_only:
        query += " WHERE is_active=1"
    query += " ORDER BY total_commits DESC"
    with _conn() as conn:
        rows = conn.execute(query).fetchall()
    return [_deserialise_profile(r) for r in rows]


def get_agent_stats() -> dict:
    """
    Aggregate breakdown of AI tool usage across all commits.
    Reads from enriched gh_commits (always available) + project_profiles (when profiled).
    """
    _init_intel_tables()
    with _conn() as conn:
        # Primary source: enriched commits (always populated after enrich_commits_with_agents)
        commit_rows = conn.execute("""
            SELECT agent_tool, claude_model, COUNT(*) as cnt
            FROM gh_commits
            WHERE agent_tool IS NOT NULL AND agent_tool != ''
            GROUP BY agent_tool, claude_model
            ORDER BY cnt DESC
        """).fetchall()

        total_commits = conn.execute("SELECT COUNT(*) FROM gh_commits").fetchone()[0]
        total_repos = conn.execute("SELECT COUNT(DISTINCT repo) FROM gh_commits").fetchone()[0]

        # Per-repo agent breakdown
        repo_agents = conn.execute("""
            SELECT repo, owner,
                   SUM(CASE WHEN agent_tool IN ('claude-code','mixed') THEN 1 ELSE 0 END) as claude_cnt,
                   SUM(CASE WHEN agent_tool = 'cursor' THEN 1 ELSE 0 END) as cursor_cnt,
                   SUM(CASE WHEN agent_tool = 'human' THEN 1 ELSE 0 END) as human_cnt,
                   COUNT(*) as total
            FROM gh_commits
            GROUP BY repo, owner
            ORDER BY total DESC
            LIMIT 20
        """).fetchall()

    by_agent: dict[str, int] = {}
    by_model: list[dict] = []
    for r in commit_rows:
        agent = r["agent_tool"] or "human"
        by_agent[agent] = by_agent.get(agent, 0) + r["cnt"]
        if r["claude_model"]:
            by_model.append({"claude_model": r["claude_model"], "cnt": r["cnt"]})

    claude_count = sum(v for k, v in by_agent.items() if k in ("claude-code", "mixed"))
    cursor_count = by_agent.get("cursor", 0) + sum(v for k, v in by_agent.items() if k == "mixed")
    human_count  = by_agent.get("human", 0)
    top_agent    = max(by_agent, key=lambda k: by_agent[k]) if by_agent else "human"

    return {
        "total_commits": total_commits,
        "total_repos": total_repos,
        "by_agent": by_agent,
        "by_model": by_model,
        "claude_pct": round(100 * claude_count / total_commits, 1) if total_commits else 0,
        "cursor_pct": round(100 * cursor_count / total_commits, 1) if total_commits else 0,
        "human_pct":  round(100 * human_count  / total_commits, 1) if total_commits else 0,
        "top_agent": top_agent,
        "repo_breakdown": [dict(r) for r in repo_agents],
    }


def search_profiles(query: str) -> list[dict]:
    """
    Full-text search across purpose, description, and readme_summary.

    Args:
        query: Freeform search string. Searched as substring (case-insensitive).

    Returns:
        Matching profiles ordered by total_commits DESC.
    """
    _init_intel_tables()
    pattern = f"%{query}%"
    with _conn() as conn:
        rows = conn.execute(
            """SELECT * FROM project_profiles
               WHERE purpose LIKE ?
                  OR description LIKE ?
                  OR readme_summary LIKE ?
                  OR repo LIKE ?
                  OR display_name LIKE ?
               ORDER BY total_commits DESC""",
            (pattern, pattern, pattern, pattern, pattern),
        ).fetchall()
    return [_deserialise_profile(r) for r in rows]


# ── Fix / problem pattern extractor ──────────────────────────────────────────

_FIX_COMMIT_RE = re.compile(
    r'^(?:fix|bug|resolve|patch|hotfix|repair|revert)[\s(:!]',
    re.IGNORECASE,
)
_FIX_PR_TITLE_RE = re.compile(
    r'\b(?:fix(?:ed|es)?|bug|broken|resolve[sd]?|error|crash|fail(?:ed|s)?|'
    r'regression|hotfix|patch|revert)\b',
    re.IGNORECASE,
)
_HOW_FIXED_RE = re.compile(
    r'(?:by|via|using|with|through|replaced?|updated?|added?|removed?|increased?|'
    r'decreased?|changed?|switched?|migrated?)\s+.{5,100}',
    re.IGNORECASE,
)


def _extract_how_fixed(message: str) -> str:
    """
    Try to pull the 'how' out of a commit message body.
    Falls back to the first non-empty body line.
    """
    lines = message.split("\n")
    body_lines = [l.strip() for l in lines[1:] if l.strip() and not l.startswith("Co-")]
    for line in body_lines:
        m = _HOW_FIXED_RE.search(line)
        if m:
            return line[:200]
    return body_lines[0][:200] if body_lines else ""


def extract_fix_patterns(repo: str | None = None) -> list[dict]:
    """
    Query cached commits and PRs to find fix/bug events.

    Args:
        repo: Optional repo name filter. None = all repos.

    Returns list of FixPattern dicts with:
        repo, owner, date, what_broke, how_fixed, agent, model,
        pr_number, days_to_fix, commit_sha, source
    """
    _init_intel_tables()
    results: list[dict] = []

    # ── Part A: fix: commits ──────────────────────────────────────────────────
    commit_query = """
        SELECT sha, repo, owner, message, author_date, pr_number, additions, deletions
        FROM gh_commits
        WHERE commit_type = 'fix'
    """
    params: list[Any] = []
    if repo:
        commit_query += " AND repo=?"
        params.append(repo)
    commit_query += " ORDER BY author_date DESC LIMIT 2000"

    with _conn() as conn:
        commit_rows = conn.execute(commit_query, params).fetchall()

    for row in commit_rows:
        msg = row["message"] or ""
        subject = msg.split("\n")[0].strip()
        attr = detect_agents(msg)

        # days_to_fix: try to match against a PR
        days_to_fix = 0.0
        pr_num = row["pr_number"] or 0

        if pr_num:
            with _conn() as conn:
                pr_row = conn.execute(
                    """SELECT julianday(merged_at) - julianday(created_at) as d
                       FROM gh_pull_requests
                       WHERE repo=? AND owner=? AND number=?""",
                    (row["repo"], row["owner"], pr_num),
                ).fetchone()
            if pr_row and pr_row["d"] is not None:
                days_to_fix = round(float(pr_row["d"]), 2)

        results.append(FixPattern(
            repo=row["repo"],
            owner=row["owner"],
            date=row["author_date"][:10] if row["author_date"] else "",
            what_broke=subject,
            how_fixed=_extract_how_fixed(msg),
            agent=attr.agent_tool,
            model=attr.claude_model,
            pr_number=pr_num,
            days_to_fix=days_to_fix,
            commit_sha=row["sha"][:7],
            source="commit",
        ).model_dump())

    # ── Part B: fix PRs ───────────────────────────────────────────────────────
    pr_query = """
        SELECT number, repo, owner, title, body, merged_at, created_at,
               julianday(merged_at) - julianday(created_at) as days_open
        FROM gh_pull_requests
        WHERE merged_at IS NOT NULL
          AND (
            title LIKE '%fix%' OR title LIKE '%bug%' OR title LIKE '%broken%'
            OR title LIKE '%resolve%' OR title LIKE '%error%' OR title LIKE '%revert%'
            OR title LIKE '%crash%' OR title LIKE '%fail%' OR title LIKE '%hotfix%'
          )
    """
    pr_params: list[Any] = []
    if repo:
        pr_query += " AND repo=?"
        pr_params.append(repo)
    pr_query += " ORDER BY merged_at DESC LIMIT 500"

    with _conn() as conn:
        pr_rows = conn.execute(pr_query, pr_params).fetchall()

    for row in pr_rows:
        body = row["body"] or ""
        attr = detect_agents(body)
        results.append(FixPattern(
            repo=row["repo"],
            owner=row["owner"],
            date=(row["merged_at"] or "")[:10],
            what_broke=row["title"],
            how_fixed=_extract_how_fixed(body),
            agent=attr.agent_tool,
            model=attr.claude_model,
            pr_number=row["number"],
            days_to_fix=round(float(row["days_open"] or 0), 2),
            commit_sha="",
            source="pr",
        ).model_dump())

    # Deduplicate by (repo, pr_number) — prefer commit source
    seen: set[tuple[str, int]] = set()
    deduped: list[dict] = []
    for item in results:
        key = (item["repo"], item["pr_number"])
        if item["pr_number"] and key in seen:
            continue
        if item["pr_number"]:
            seen.add(key)
        deduped.append(item)

    # Sort: most recent first
    deduped.sort(key=lambda x: x.get("date", ""), reverse=True)
    return deduped


