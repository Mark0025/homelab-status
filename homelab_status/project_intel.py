# ── Enrich gh_commits with agent columns ─────────────────────────────────────

def enrich_commits_with_agents() -> int:
    """
    Backfill agent_tool and claude_model columns on gh_commits rows
    that haven't been classified yet.

    Returns number of rows updated.
    """
    _init_intel_tables()
    with _conn() as conn:
        rows = conn.execute(
            "SELECT sha, repo, message FROM gh_commits WHERE agent_tool = '' OR agent_tool IS NULL"
        ).fetchall()

    if not rows:
        return 0

    updates: list[tuple[str, str, str, str]] = []
    for row in rows:
        attr = detect_agents(row["message"] or "")
        updates.append((attr.agent_tool, attr.claude_model, row["sha"], row["repo"]))

    with _conn() as conn:
        conn.executemany(
            "UPDATE gh_commits SET agent_tool=?, claude_model=? WHERE sha=? AND repo=?",
            updates,
        )
    logger.debug(f"Enriched {len(updates)} commits with agent attribution")
    return len(updates)
