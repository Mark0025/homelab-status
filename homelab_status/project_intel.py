# ── Pydantic models ──────────────────────────────────────────────────────────

class AgentAttribution(BaseModel):
    """Which AI tool(s) created / contributed to a commit."""
    agent_tool: str = "human"          # 'claude-code' | 'cursor' | 'human' | 'copilot' | 'aider' | 'mixed'
    claude_model: str = ""             # e.g. 'Claude Opus 4.6', 'Claude Sonnet 4.5'
    cursor_used: bool = False
    coderabbit_reviewed: bool = False
    other_agents: list[str] = Field(default_factory=list)


class ProjectProfile(BaseModel):
    """Complete intelligence profile for one GitHub repository."""
    repo: str
    owner: str

    # Identity
    display_name: str = ""
    description: str = ""
    purpose: str = ""
    what_it_does_not_do: str = ""
    tech_stack: list[str] = Field(default_factory=list)

    # Infrastructure linkage
    container_name: str = ""
    docker_networks: list[str] = Field(default_factory=list)
    connects_to: list[str] = Field(default_factory=list)
    public_url: str = ""

    # AI tool attribution
    primary_agent: str = ""            # 'claude-code' | 'cursor' | 'human' | 'mixed'
    claude_model: str = ""
    cursor_used: bool = False
    coderabbit_used: bool = False
    other_agents: list[str] = Field(default_factory=list)

    # Development health
    open_issues: int = 0
    total_commits: int = 0
    total_prs: int = 0
    fix_commit_ratio: float = 0.0
    avg_pr_days: float = 0.0
    last_active: str = ""

    # Content extracted from README/CLAUDE.md
    readme_summary: str = ""
    claude_md_exists: bool = False
    readme_exists: bool = False

    # Status flags
    is_active: bool = True
    is_fork: bool = False
    has_docker: bool = False
    has_tests: bool = False
    has_ci: bool = False

    # Revenue readiness
    payment_provider: str = ""   # 'stripe' | 'polar' | 'none'
    has_pricing_ui: bool = False  # pricing route/component exists in repo
    has_payment_webhook: bool = False  # webhook route for stripe/polar exists

    profiled_at: str = ""


